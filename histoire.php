                <p class="petitParagraphe col-md-4">
                    A tout jamais ? ... Non ! en 2015 Didière et Hadrien Parys, tous deux descendants de Séraphin et de Josette, décident de faire renaitre de leurs cendres les
                    procédés, les recettes, le raffinement d’antan, ... des « Sucreries Rochelaises » !<br><br>
                    Depuis mars 2017 les ateliers, maintenant situés à Reims, en s’inscrivant dans une démarche Haute Qualité Environnementale, produisent 300 Kg de « gourmandise » 
                    par jour, dont 200 à base de chocolat.<br><br>
                    La marque « Gourmandise, Séraphin Parys » propose ainsi une large gamme de sucreries raffinées. L’entreprise « Gourmandise-SARL » qui les produit dispose,
                    parallèlement à ses circuits de distribution, d’un espace de vente directe au 19 rue de Bioko à Reims dans la Marne.
