                    C’est à l’occasion d’un voyage en Angleterre en 1786 que le jeune Séraphin Parys, à peine âgé de 20 ans, rencontre l’industriel Joseph 
                    Storrs Fry, inventeur de génie qui venait de faire naitre la passion du chocolat à travers toute l’Europe !<br><br>
                    De retour en France, il ne lui faudra pas moins de 25 ans pour donner naissance au concept de fabrication et de distribution de confiseries
                    de luxe à base 
                    de chocolat de grande qualité (avec un approvisionnement auprès des manufactures de J. S. Fry & Sons. à Bristol).<br><br>
