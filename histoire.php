                <p class="petitParagraphe col-md-4">
                    Les « Sucreries Rochelaises » de Séraphin Parys ouvrent en 1811 à La Rochelle.
                    Cette entreprise traverse avec succès et distinction les grandes crises du XIXème siècle. En 1900, Josette Parys, arrière-arrière-petite-fille 
                    de Séraphin, aux commandes des « Sucreries Rochelaises », remportera une médaille d’Or lors de l’Exposition Universelle de Paris pour son produit 
                    phare le « Caracao » !<br><br>
                    Le début des années 30 avec la crise, le contexte de guerre, les pénuries d’aliments (farine, lait, œufs, ...) est très défavorable au commerce ...
                    en 1937, les « Sucreries Rochelaises » ferment pour disparaitre à tout jamais.<br><br>
                </p>
